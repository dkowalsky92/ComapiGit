import UIKit

class Colors: NSObject {
    class func darkGreenBlack() -> UIColor {
        return UIColor.init(red: 28 / 255.0, green: 35 / 255.0, blue: 33 / 255.0, alpha: 1.0)
    }
    class func weldonBlue() -> UIColor {
        return UIColor.init(red: 125 / 255.0, green: 152 / 255.0, blue: 161 / 255.0, alpha: 1.0)
    }
    class func cadetGray() -> UIColor {
        return UIColor.init(red: 94 / 255.0, green: 101 / 255.0, blue: 114 / 255.0, alpha: 1.0)
    }
    class func wildBlueYonder() -> UIColor {
        return UIColor.init(red: 169 / 255.0, green: 180 / 255.0, blue: 194 / 255.0, alpha: 1.0)
    }
    class func isabellineWhite() -> UIColor {
        return UIColor.init(red: 238 / 255.0, green: 241 / 255.0, blue: 239 / 255.0, alpha: 1.0)
    }
}
